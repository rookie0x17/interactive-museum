<div class="container"><div class="columns"><div class="column is-8 is-offset-2"><h3><span style="color: #333333;"><b>How Does the 3D Hologram Pyramid Work?</b></span></h3>
The hologram pyramid is a simple device that can be made by manipulating a sheet of plastic into the shape of a pyramid with its top cut off. The device creates a 3D-like illusion for the viewer and makes an image or video appear as if it were in midair. It works on the principle of <a href="https://en.wikipedia.org/wiki/Pepper%27s_ghost" target="_blank">Pepper's Ghost</a>. Four symmetrically opposite variations of the same image are projected onto the four faces of the&nbsp;pyramid. By principle, each side projects the image falling on it to the center of the pyramid. These projections work in unison to form a whole figure, which creates a 3D illusion.
