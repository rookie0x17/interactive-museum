<div class="container"><div class="columns"><div class="column is-8 is-offset-2"><p><strong>NOTE:</strong> If you do not have access to a printer, you can also make a template on your own. Draw a basic "Trapezoid" on a piece of paper using these dimensions. Parallel sides= 1cm, 6cm &amp; other two sides= 4.5cm each. You can always double or triple the dimensions in proportion to use on a larger display.
<br><br>2. Trace out the shape on the plastic sheet using a ruler and pen. For the trapezoid template, trace out four similar outlines on the plastic sheet. Now carefully&nbsp;cut out the outlines with a cutting blade and ruler. Try to make your cuts as precise as possible for a perfect pyramid.
<br><span style="font-weight: 400;"><br>3. </span><strong>If you used the printout template:</strong><span style="font-weight: 400;"> Score the red edges very lightly using a cutting blade. This will allow you to fold the edges better and form a pyramid shape.&nbsp;Stick the open edges of the sheet together using clear cellotape.
<br></span><strong><br>If you used the trapezoid template:</strong><span style="font-weight: 400;"> Join the four edges to form a pyramid shape.&nbsp;Stick them together.
<br><br>Either way, in the end you will have a pyramid like the one shown below.</span><br></p></div></div></div>

